// Helper function to get mock data based on type
export const getMockData = (type, filters = {}) => {
  let data = [];
  
  switch (type) {
    case 'design_trends':
      data = mockDesignTrends;
      break;
    case 'viral_campaigns':
      data = mockViralCampaigns;
      break;
    case 'trending_hashtags':
      data = mockTrendingHashtags;
      break;
    case 'creative_news':
      data = mockCreativeNews;
      break;
    case 'featured_insights':
      data = mockFeaturedInsights;
      break;
    default:
      data = [];
  }

  // Apply filters
  if (filters.region) {
    data = data.filter(item => item.regions?.includes(filters.region));
  }
  
  if (filters.category) {
    data = data.filter(item => item.category_id === filters.category);
  }
  
  if (filters.featured) {
    data = data.filter(item => item.is_featured);
  }

  return data;
};
